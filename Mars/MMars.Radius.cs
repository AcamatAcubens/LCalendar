	/// <param name="value">Genauigkeitskennung.</param>
	/// <param name="t">Jahrhundertbruchteil.</param>
	/// <returns>Liefert die Summe der Terme 5. Ordnung.</returns>
	private static double R5(EPrecision value, double t)
	{
		// Lokale Felder einrichten
		double rtn = 0.0;
		if(value == EPrecision.Low)    return rtn;
		if(value == EPrecision.Medium) return rtn;

		// Terme aufsummieren
		rtn += 0.00000000476 * MMath.Cos(2.47617204701 +  6681.22485339960 * t);
		rtn += 0.00000000268 * MMath.Cos(2.91510547706 + 10021.83728009940 * t);
		rtn += 0.00000000115 * MMath.Cos(1.76888962311 +  3340.61242669980 * t);
		rtn += 0.00000000096 * MMath.Cos(3.31378377179 + 13362.44970679920 * t);
		return rtn;
	}

