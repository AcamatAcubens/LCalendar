	public static double Radius(EPrecision value, double jd)
	{
		// Lokale Felder einrichten
		double t  = (jd - MCalendar.Jdn20000101) / 365250.0; // Jahrhundertbruchteil
		double r0 = MJupiter.R0(value, t);
		double r1 = MJupiter.R1(value, t);
		double r2 = MJupiter.R2(value, t);
		double r3 = MJupiter.R3(value, t);
		double r4 = MJupiter.R4(value, t);
		double r5 = MJupiter.R5(value, t);

		// Radius berechnen
		return MMath.Polynome(t, r0, r1, r2, r3, r4, r5);
	}
